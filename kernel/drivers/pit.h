/* =============================================================================
 * VortexOS — kernel/drivers/pit.h
 * PIT (8253/8254) — Programmable Interval Timer
 * ============================================================================= */
#ifndef VOS_PIT_H
#define VOS_PIT_H

#include "types.h"

/* Инициализировать PIT на заданную частоту (Hz).
 * Рекомендуется 100 Hz -> квант ~10 мс. */
void pit_init(uint32_t hz);

/* Текущий счётчик тиков с момента старта */
uint64_t pit_ticks(void);

/* Забрать запрос на отрисовку кадра, выставленный PIT с троттлингом (~50 FPS).
 * Возвращает 1 один раз на каждый «тик рендера». Зовётся из задачи рендера. */
int pit_consume_render_request(void);

#endif /* VOS_PIT_H */
